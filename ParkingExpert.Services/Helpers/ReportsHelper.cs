using System;

namespace ParkingExpert.Services.Helpers
{
    public static class ReportsHelper
    {
        public static string ToMonth(this int month)
        {
            switch (month)
            {
                case 1:
                    return "January";
                case 2:
                    return "February";
                case 3:
                    return "March";
                case 4:
                    return "April";
                case 5:
                    return "May";
                case 6:
                    return "June";
                case 7:
                    return "July";
                case 8:
                    return "August";
                case 9:
                    return "September";
                case 10:
                    return "October";
                case 11:
                    return "November";
                case 12:
                    return "December";
                default:
                    throw new AggregateException();
            }
        }
        
        public static string ToQuarter(this int quarter)
        {
            switch (quarter)
            {
                case 0:
                    return "1st";
                case 1:
                    return "2nd";
                case 2:
                    return "3rd";
                case 3:
                    return "4th";
                default:
                    throw new AggregateException();
            }
        }
    }
}